"""Compare 2-point correlator G^(2)(r) in lambda phi^4 between NNFT and analytics.

Compares curves over a 1D radial grid r:
  - tree-level free propagator with UV regulator from asymptotic expansion
  - one-loop tadpole, no IR regulator via Schwinger parameterization
  - one-loop tadpole, with Gaussian IR vertex regulator via Schwinger parameterization
  - NNFT method A: free-theory sampling reweighted by exp(-S_int)
  - NNFT method B: Metropolis-Hastings on the modified PDF
  - NNFT method C: Hamiltonian Monte Carlo on the modified PDF
  - NNFT method D: MALA on the modified PDF
  - Lattice HMC (if matching data/lattice_g2_l{lambda}.npz is present,
    generated by lattice_correlators.py)

Saves data to data/lambda_phi4/compare_g2.npz and a plot to .../compare_g2.png.
"""

